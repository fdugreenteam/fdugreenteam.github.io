  <h1>Constitution</h1>
  <p>
    <strong>Article I</strong><br />
    The purpose of this club is to engage students in understanding the importance of reducing the use of natural resources,  minimize pollution and waste. It is also our intent to develop an avenue for information exchange and community outreach to help create an equitable and sustainable future.  The university is a microcosm of the larger community, and the manner in which it carries out its daily activities is an important demonstration of alternatives to achieve environmentally responsible living.
  </p>
  <p>
    <strong>Article II</strong><br />
    Meetings
    <ol>
      <li>Membership is restricted to members of the Fairleigh Dickinson University community</li>
      <li>There shall be no restriction on membership based on race, religion, nationality, gender, physical ability, sexual orientation or preference or political conviction.</li>
      <li>Membership will be free</li>
      <li>Members must be interested and willing to participate and support environment activities.</li>
    </ol>
  </p>
  <p>
    <strong>Article III</strong><br />
    <ol>
      <li>The board of the organization shall consist of President, Vice-President, Treasurer, Secretary, Technology Manager, SGA representative, Parliamentarian and public relations officer.</li>
      <li>Duties and Powers
        <ol type="a">
          <li>President shall
            <ol type="i">
              <li>Preside over all meetings</li>
              <li>organize special meetings</li>
              <li>appoint special committee</li>
              <li>oversee all functions taking place within the club</li>
              <li>Meet with mentor twice a month</li>
            </ol>
          </li>
          <li>Vice-President shall
            <ol type="i">
              <li>perform all duties of the President in his/her absence</li>
              <li>Foster membership development</li>
              <li>Find new venues for advertising the club</li>
              <li>Promote and coordinate club activities</li>
              <li>Ensure that the members of the club are performing their duties successfully.</li>
            </ol>
          </li>
          <li>Treasurer shall
            <ol type="i">
              <li>Keep an accurate and complete record of all monetary transactions</li>
              <li>be responsible for club bank account</li>
              <li>prepare and present to the Executive Board quarterly reports</li>
              <li>Must disburse funds with the approval of the executive board in accordance with the regulations of the Student Government Association.</li>
              <li>Attend all treasure meetings</li>
            </ol>
          </li>
          <li>Secretary shall
            <ol type="i">
              <li>Be responsible for maintaining an accurate and permanent record of the organization and its activities.</li>
              <li>Provide advisor with copies of all club activities.</li>
              <li>Prepare all correspondence</li>
              <li>Notify the Office of Student Life and Mentor of all meetings and activities, make necessary reports etc.</li>
              <li>Serve as liaison between the Club and the Green Campus Committee.</li>
            </ol>
          </li>
          <li>Technology Manager
            <ol type="i">
              <li>Be responsible for managing club website and other technology services</li>
              <li>Maintain up to date information on the site and event calendar</li>
              <li>Facilitate access between the other club members and the services</li>
              <li>SGA representative</li>
              <li>Attend all SGA meetings and report to the president</li>
              <li>Facilitate the communications between club e-board and the SGA.</li>
              <li>Inform all pertinent information obtained from SGA to the appropriate e-board member.</li>
            </ol>
          </li>
          <li>Parliamentarian
            <ol type="i">
              <li>Serve as a liaison between the members and the e-board and other organizations.</li>
              <li>Assist the secretary on his/her duties of notifying the Office of Student Life and Mentor of all meetings and activities.</li>
              <li>Report all actions to the President</li>
            </ol>
          </li>
          <li>Public relation representative
            <ol type="i">
              <li>Recruit new members</li>
              <li>Assist the secretary on his/her duties of notifying all members and meeting activities</li>
              <li>Be responsible for the advertisement of events (creating and distributing posters)</li>
              <li>Attend student related campus activities</li>
            </ol>
          </li>
        </ol>
      </li>
    </ol>
  </p>
  <p>
    <strong>Article IV</strong><br />
    Meetings
    <ol>
      <li>Elections will be held every year in the month of March.</li>
      <li>Elections will be held for the post of Vice-President, Secretary and Treasurer.</li>
      <li>Any member of the FDU community with a GPA of 2.5 or above can run for the above position</li>
      <li>To run for the post of President, the person should have been on the e-board for at least one semester.</li>
      <li>In case of a tie, the candidates will give a speech explaining their qualifications and intentions. Another vote will follow.</li>
    </ol>
  </p>
  <p>
    <strong>Article V</strong><br />
    Meetings
    <ol>
      <li>There will be bi-weekly meetings</li>
      <li>The executive board shall decide the day and time of the meeting.</li>
      <li>Special meetings can be called at the request of any board member with a valid reason.</li>
      <li>Attendance is mandatory for the board members</li>
      <li>Any board member should inform the president prior to the meeting if they are not able to attend the meeting.</li>
    </ol>
  </p>
  <p>
    <strong>Article VI</strong><br />
    Finances
    <ol>
      <li>Allocations of funds will be sought directly from Student Government Association</li>
      <li>Funds will also be sought through various fundraising activities</li>
      <li>All added revenue earned would be kept in the club bank account.</li>
    </ol>
  </p>
  <p>
    <strong>Article VII</strong><br />
    <ol>
      <li>An executive member can be suspended and/or terminated from the club for the following reason
        <ol type="a">
          <li>Not showing in meeting for 3 consecutive weeks without prior information</li>
          <li>Repeated failure to meet assigned responsibilities to the club</li>
        </ol>
      </li>
      <li>Disciplinary Actions for the offenses shall be as follows:
        <ol type="a">
          <li>First offense: verbal warning</li>
          <li>Second offense: written warning</li>
          <li>Third offense: suspension from all club functions for a period of time to be determined by the executive board</li>
          <li>Fourth Offense: membership termination and future rights of membership revoked.</li>
        </ol>
      </li>
    </ol>
  </p>
  <p>
    <strong>Article VIII</strong><br />
    Amendments
    <ol>
      <li>The constitution may be amended at any time in which a quorum is present.</li>
      <li>All changes require a majority vote of the members and the approval of the board.</li>
    </ol>
  </p>
  <p>
    <strong>Article IX</strong><br />
    This constitution can be changed or amended any time, after approval by the officers and after from the members favoring such amendments by a two majority vote.
  </p>
  <p>
    <strong>Article X</strong><br />
    Officer
    <ol>
      <li>Officers must be full-time registered Fairleigh Dickinson University students in good academic, financial and disciplinary standards.</li>
      <li>Officers must maintain a CGPA of at least 2.5 and attend all e-board meetings.</li>
      <li>Officer representative must attend SGA, Presidents Meeting, Allocations meeting and green campus committee meetings.</li>
    </ol>
  </p>
